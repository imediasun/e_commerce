                                <i class="arrow"></i> 
                                            </a>';
                                                //рекурсия - проверяем нет ли дочерних категорий
                                                $this->view_cat($arr, $arr[$parent_id][$i]->id, 1);
                                                echo '</li> ';
                                            }
                                            if($parent_id !== 0) {
                                                echo '</ul>';
                                            }
                                        }
